    //btn Cadastrar
    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        String nome = txfNomeProduto.getText();
        String marca = txfMarca.getText();
        int tamanho = Integer.parseInt(txfTamanho.getText());
        String cor = txfCor.getText();
        String descricao = txfObservacao.getText();
        double valorcusto = Double.parseDouble(txfValorCusto.getText());
        double valorvenda = Double.parseDouble(txfValorVenda.getText());
        double margemlucro = Double.parseDouble(txfMargemLucro.getText());
        int qntprodutoestoque = Integer.parseInt(txfQntProdutoEstoque.getText());

        Produto p = new Produto();

        p.setNome(nome);
        p.setMarca(marca);
        p.setTamanho(tamanho);
        p.setCor(cor);
        p.setDescricao(descricao);
        p.setValorCusto(valorcusto);
        p.setValorVenda(valorvenda);
        p.setMargemLucro(margemlucro);
        p.setQntProdutoEstoque(qntprodutoestoque);

        ProdutoDAO dao = new ProdutoDAO();
        JOptionPane.showMessageDialog(null, dao.inserirProduto(p));


    }//GEN-LAST:event_btnCadastrarActionPerformed

