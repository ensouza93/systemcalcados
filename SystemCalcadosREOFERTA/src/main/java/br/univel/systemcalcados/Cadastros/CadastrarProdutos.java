    // btn Excluir
    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        try {
            int op = JOptionPane.showConfirmDialog(null, "Deseja Remover " + txfNomeProduto.getText() + "?");

            if (op == JOptionPane.YES_OPTION) {
                ProdutoDAO dao = new ProdutoDAO();
                int idproduto = Integer.parseInt(txfCodigo.getText());
                JOptionPane.showMessageDialog(null, dao.Excluir(idproduto));
                limpar();
            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Selecione um Item na lista");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed

        try {
            double valorcusto = Double.parseDouble(txfValorCusto.getText());
            double margemlucro = Double.parseDouble(txfMargemLucro.getText());
            //   String aux1 = txfPrecoTotal.getText();
//            double valorproduto = Double.parseDouble(cbxValorProduto.getSelectedItem().toString());

            double valorvenda = valorcusto * margemlucro / 100;
            String texto = Double.toString(valorvenda + valorcusto);

            txfValorVenda.setText(texto);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Valores Inválidos");
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnCalcularActionPerformed

