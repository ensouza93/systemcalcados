    // btn Excluir
    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        ClienteDAO dao = new ClienteDAO();
        try {
            int op = JOptionPane.showConfirmDialog(null, "Deseja Remover " + txfNome.getText() + txfSobrenome.getText() + "?" + "?");

            if (op == JOptionPane.YES_OPTION) {
                JOptionPane.showMessageDialog(null, dao.Excluir(Integer.parseInt(txfCodCliente.getText())));
                limpar();
            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Selecione um cliente na lista");
        }


    }//GEN-LAST:event_btnExcluirActionPerformed

