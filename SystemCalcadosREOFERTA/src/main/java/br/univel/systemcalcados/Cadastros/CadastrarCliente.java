    // btn Cadastrar
    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        String nome = txfNome.getText();
        String sobrenome = txfSobrenome.getText();
        String endereco = txfEndereco.getText();
        String bairro = txfBairro.getText();
        String cidade = txfCidade.getText();
        String cpf = txffCPF.getText();
        String rg = txffRG.getText();
        String telres = txffTelefoneRes.getText();
        String telcelular = txffCelular.getText();
        String email = txfEmail.getText();
        String refpessoala_nomecompleto = txfNomeCompletoRefPessoalA.getText();
        String reftrabalhoa_nomeempresa = txfRefTrabalhoA_NomeEmpresa.getText();
        String reftrabalhoa_rendasalarial = txffRendaSalarial.getText();
        String reftraballhoa_temposervico = txfTempoServico.getText();
        String observacao = txfObservacoes.getText();
        String cep = txffCEP.getText();
        String refpessoalb_nomecompleto = txffNomeCompletoRefPessoalB.getText();
        String refpessoaa_telefone = txffTelefoneRefPessoalA.getText();
        String refpessoala_celular = txffCelularRefPessoalA.getText();
        String refpessoalb_telefone = txffTelefoneRefPessoalB.getText();
        String refpessoalb_celular = txffCelularRefPessoalB.getText();
        String reftrabalhoa_telefone = txffRefTrabalhoATelefone.getText();

        try {
            Cliente c1 = new Cliente(nome, sobrenome, endereco, bairro, cidade, cpf, rg, telres, telcelular, email, refpessoala_nomecompleto, reftrabalhoa_nomeempresa, reftrabalhoa_rendasalarial,
                    reftraballhoa_temposervico, observacao, cep, refpessoalb_nomecompleto, refpessoaa_telefone, refpessoala_celular, refpessoalb_telefone, refpessoalb_celular, reftrabalhoa_telefone);

            ClienteDAO dao = new ClienteDAO();

            if (!c1.ValidarCPF(cpf)) {
                JOptionPane.showMessageDialog(null, "Campo CPF em branco ou inválido.");
                txffCPF.requestFocus();
            } else {
                if (c1.validEmail(email)) {
                    JOptionPane.showMessageDialog(null, dao.inserir(c1));

                } else {
                    JOptionPane.showMessageDialog(null, "Campo Email Inválido");
                    txfEmail.requestFocus();
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Verifique se os campos foram digitados corretamente");

        }


    }//GEN-LAST:event_btnCadastrarActionPerformed

