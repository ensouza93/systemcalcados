    // btn editar
    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        try {
            ContasPagarDAO dao = new ContasPagarDAO();

            int id = Integer.parseInt(txfCod.getText());
            String titulo = txfTitulo.getText();
            String auxiliar = txfValor.getText().replace(",", ".");
            Double valor = Double.parseDouble(auxiliar);
            String vencimento = txffData.getText();

            ContasPagar cp = new ContasPagar(titulo, valor, vencimento);

            JOptionPane.showMessageDialog(null, dao.altera(cp, id));
            limpar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Verifique se os campos estão digitados corretamente");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

