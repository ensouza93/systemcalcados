    public void loadContasPagar(ContasPagar contasPagarSelected) {

        try {

            conta = contasPagarSelected;

            txfCod.setText(Integer.toString(conta.getId()));
            txfTitulo.setText(conta.getTitulo());
            txfValor.setText(Double.toString(conta.getValor()));
            txffData.setText(conta.getVencimento());

        } catch (Exception e) {

        }

    }
