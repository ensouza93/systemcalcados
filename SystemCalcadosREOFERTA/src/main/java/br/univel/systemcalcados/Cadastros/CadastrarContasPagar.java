    // btn excluir
    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        try {
            int op = JOptionPane.showConfirmDialog(null, "Deseja Remover " + txfTitulo.getText() + "?");

            if (op == JOptionPane.YES_OPTION) {
                ContasPagarDAO dao = new ContasPagarDAO();
                int cod = Integer.parseInt(txfCod.getText());
                JOptionPane.showMessageDialog(null, dao.Excluir(cod));
                limpar();
            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Selecione um Item na lista");
        }


    }//GEN-LAST:event_btnExcluirActionPerformed

