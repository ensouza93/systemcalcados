    //btn Editar
    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        try {
            int id = Integer.parseInt(txfCod.getText());
            String forma = txfFormaPagamento.getText();

            FormasPagamento form = new FormasPagamento(forma);
            FormasPagamentoDAO dao = new FormasPagamentoDAO();
            JOptionPane.showMessageDialog(null, dao.altera(form, id));
            limpar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Verifique se os campos estão digitados corretamente");
        }

    }//GEN-LAST:event_btnEditarActionPerformed

    //btn Excluir
    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        try {
            int op = JOptionPane.showConfirmDialog(null, "Deseja Remover " + txfFormaPagamento.getText() + "?");

            if (op == JOptionPane.YES_OPTION) {
                FormasPagamentoDAO dao = new FormasPagamentoDAO();
                int cod = Integer.parseInt(txfCod.getText());
                JOptionPane.showMessageDialog(null, dao.Excluir(cod));
                limpar();
            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Selecione uma Forma de pagamento na lista");
        }

    }//GEN-LAST:event_btnExcluirActionPerformed

