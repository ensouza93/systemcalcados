    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            String forma = txfFormaPagamento.getText();

            FormasPagamento fp = new FormasPagamento(forma);
            FormasPagamentoDAO dao = new FormasPagamentoDAO();

            JOptionPane.showMessageDialog(null, dao.inserir(fp));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Verifique se os campos estão digitados corretamente");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

