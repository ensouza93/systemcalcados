    //btn cadastrar
    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        String condicao;
        condicao = (txfCondicaoPagamento.getText());

        CondicoesPagamento c = new CondicoesPagamento();

        c.setCondicao(condicao);

        CondicoesPagamentoDAO dao = new CondicoesPagamentoDAO();
        JOptionPane.showMessageDialog(null, dao.inserir(c));

    }//GEN-LAST:event_btnCadastrarActionPerformed

    // btn editar
    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        int id = Integer.parseInt(txfCodCondicoesPagamento.getText());
        String condicao = txfCondicaoPagamento.getText();

        CondicoesPagamento condiP = new CondicoesPagamento(condicao);
        CondicoesPagamentoDAO dao = new CondicoesPagamentoDAO();
        JOptionPane.showMessageDialog(null, dao.altera(condiP, id));
        limpar();

    }//GEN-LAST:event_btnEditarActionPerformed

