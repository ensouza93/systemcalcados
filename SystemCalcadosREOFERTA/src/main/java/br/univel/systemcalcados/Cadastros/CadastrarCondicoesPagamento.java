    // btn excluir
    private void btnExluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExluirActionPerformed

        try {
            int op = JOptionPane.showConfirmDialog(null, "Deseja Remover " + txfCondicaoPagamento.getText() + "?");

            if (op == JOptionPane.YES_OPTION) {
                CondicoesPagamentoDAO dao = new CondicoesPagamentoDAO();
                int id = Integer.parseInt(txfCodCondicoesPagamento.getText());
                JOptionPane.showMessageDialog(null, dao.Excluir(id));
                limpar();
            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Selecione uma condicao de pagamento na lista");
        }
    }//GEN-LAST:event_btnExluirActionPerformed

