    //btn Excluir   
    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        FuncionarioDAO dao = new FuncionarioDAO();
        try {
            int op = JOptionPane.showConfirmDialog(null, "Deseja Remover " + txfNome.getText() + txfSobrenome.getText() + "?" + "?");

            if (op == JOptionPane.YES_OPTION) {
                JOptionPane.showMessageDialog(null, dao.Excluir(Integer.parseInt(txfCod.getText())));
                limpar();
            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Selecione um funcinario na lista");
        }


    }//GEN-LAST:event_btnExcluirActionPerformed

