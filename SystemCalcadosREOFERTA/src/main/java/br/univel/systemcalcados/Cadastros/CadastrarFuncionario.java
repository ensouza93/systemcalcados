    //btn Cadastrar
    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        String nome = txfNome.getText();
        String sobrenome = txfSobrenome.getText();
        String cpf = txffCPF.getText();
        String rg = txffRG.getText();
        String endereco = txfEndereco.getText();
        String bairro = txfBairro.getText();
        String cidade = txfCidade.getText();
        String cep = txfCEP.getText();
        String telcelular = txffCelular.getText();
        String telres = txffTelRes.getText();
        String email = txfEmail.getText();
        String cargo = txfCargo.getText();
        String observacao = txfObservacao.getText();

        try {

            Funcionario f1 = new Funcionario(nome, sobrenome, cpf, rg, endereco, bairro, cidade, cep, telcelular, telres, email, cargo, observacao);

            FuncionarioDAO dao = new FuncionarioDAO();

            if (!f1.ValidarCPF(cpf)) {
                JOptionPane.showMessageDialog(null, "Campo CPF em branco ou inválido.");
                txffCPF.requestFocus();
            } else {
                if (f1.validEmail(email)) {
                    JOptionPane.showMessageDialog(null, dao.inserir(f1));

                } else {
                    JOptionPane.showMessageDialog(null, "Campo Email Inválido");
                    txfEmail.requestFocus();
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Verifique se os campos foram digitados corretamente");

        }

    }//GEN-LAST:event_btnCadastrarActionPerformed

