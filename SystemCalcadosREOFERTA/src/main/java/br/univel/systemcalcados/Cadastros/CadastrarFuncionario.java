    private void limpar() {

        txfNome.setText("");
        txfSobrenome.setText("");
        txffCPF.setText("");
        txffRG.setText("");
        txfEndereco.setText("");
        txfBairro.setText("");
        txfCidade.setText("");
        txfCEP.setText("");
        txffCelular.setText("");
        txffTelRes.setText("");
        txfEmail.setText("");
        txfCargo.setText("");
        txfObservacao.setText("");

    }

    public void loadFuncionario(Funcionario funcionarioSelected) {

        try {

            funcionario = funcionarioSelected;
            txfCod.setText(Integer.toString(funcionario.getId()));

            txfNome.setText(funcionario.getNome());

            txfSobrenome.setText(funcionario.getSobrenome());
            txffCPF.setText(funcionario.getCpf());
            txffRG.setText(funcionario.getRg());
            txfEndereco.setText(funcionario.getEndereco());
            txfBairro.setText(funcionario.getBairro());
            txfCidade.setText(funcionario.getCidade());
            txfCEP.setText(funcionario.getCep());
            txffCelular.setText(funcionario.getCelular());
            txffTelRes.setText(funcionario.getTelRes());
            txfEmail.setText(funcionario.getEmail());
            txfCargo.setText(funcionario.getCargo());
            txfObservacao.setText(funcionario.getObservacao());

        } catch (Exception e) {
        }

    }
