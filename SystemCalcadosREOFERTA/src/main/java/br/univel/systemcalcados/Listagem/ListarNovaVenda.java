    class ModeloNovaVenda extends AbstractTableModel {

        private String[] colunas = {"CodVenda","Valor Total"};
        private List<NovaVenda> vendas = new ArrayList<>();
        NovaVendaDAO nvDao = new NovaVendaDAO();

        public ModeloNovaVenda() {
            popularTabela();
        }

        private List<NovaVenda> popularTabela() {
            vendas = nvDao.listar();
            fireTableDataChanged();
            return vendas;

        }

        @Override
        public int getRowCount() {
            return vendas.size();
        }

        @Override
        public int getColumnCount() {
            return 4;
        }

        @Override
        public String getColumnName(int i) {
            return colunas[i];
        }
        
          public NovaVenda getNovaVenda(int index) {
            return vendas.get(index);
        }

        @Override
        public Object getValueAt(int i, int i1) {

            NovaVenda venda = (NovaVenda) vendas.get(i);

             switch (i1) {
                case 0:
                    return venda.getId();

                default:
                    return venda.getPrecoTotal();
//                case 2:
//                    return venda.getProduto();
//                default:
//                    return venda.getPrecoTotal();
            }
        }

        public void busca(String texto) {

            NovaVendaDAO NVDao = new NovaVendaDAO();
            List<NovaVenda> nv = new ArrayList<>();
            nv = NVDao.listar();

            Pattern padrao = Pattern.compile(".*" + texto + ".*", Pattern.CASE_INSENSITIVE); // criou padrao .* e qualquer coisa antes ou depois

            Iterator i = nv.iterator();

            while (i.hasNext()) {
                NovaVenda novavenda = (NovaVenda) i.next();
                Matcher pesquisa = padrao.matcher(novavenda.getCliente().getNome()); // vai comparar com o getnome

                if (!pesquisa.matches()) { // resultado da pesquisa
                    i.remove();             // se nao combimou remove.
                }
            }

            // se caso dar erro ao retornar os registros na busca
            this.vendas = nv;
            fireTableDataChanged();
        }
    }
