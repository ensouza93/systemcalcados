    class ModeloContas extends AbstractTableModel {

        private String[] colunas = {"Cod", "T√≠tulo ", "Valor", "Vencimento"};
        private List<ContasPagar> contas = new ArrayList<>();
        ContasPagarDAO dao = new ContasPagarDAO();

        public ModeloContas() {
            popularTabela();
        }

        private List<ContasPagar> popularTabela() {
            contas = dao.listar();
            fireTableDataChanged();
            return contas;

        }

        @Override
        public int getRowCount() {
            return contas.size();
        }

        @Override
        public int getColumnCount() {
            return 4;
        }

        @Override
        public String getColumnName(int i) {
            return colunas[i];
        }

        public ContasPagar getContasPagar(int index) {
            return contas.get(index);
        }

        @Override
        public Object getValueAt(int i, int i1) {

            ContasPagar conta = (ContasPagar) contas.get(i);

            switch (i1) {
                case 0:
                    return conta.getId();

                case 1:
                    return conta.getTitulo();
                case 2:
                    return conta.getValor();
                default:
                    return conta.getVencimento();
            }
        }

        public void busca(String texto) {

            ContasPagarDAO dao = new ContasPagarDAO();
            List<ContasPagar> p = new ArrayList<>();
            p = dao.listar();

            Pattern padrao = Pattern.compile(".*" + texto + ".*", Pattern.CASE_INSENSITIVE); // criou padrao .* e qualquer coisa antes ou depois

            Iterator i = p.iterator();

            while (i.hasNext()) {
                ContasPagar conta = (ContasPagar) i.next();
                Matcher pesquisa = padrao.matcher(conta.getTitulo()); // vai comparar com o getnome

                if (!pesquisa.matches()) { // resultado da pesquisa
                    i.remove();             // se nao combimou remove.
                }
            }

            // se caso dar erro ao retornar os registros na busca
            this.contas = p;
            fireTableDataChanged();
        }
    }

