    class ModeloFormasPagamento extends AbstractTableModel {

        private String[] colunas = {"Cod", "Forma de Pagamento "};
        private List<FormasPagamento> contas = new ArrayList<>();
        FormasPagamentoDAO dao = new FormasPagamentoDAO();

        public ModeloFormasPagamento() {
            popularTabela();
        }

        private List<FormasPagamento> popularTabela() {
            contas = dao.listar();
            fireTableDataChanged();
            return contas;

        }

        @Override
        public int getRowCount() {
            return contas.size();
        }

        @Override
        public int getColumnCount() {
            return 2;
        }

        @Override
        public String getColumnName(int i) {
            return colunas[i];
        }

        public FormasPagamento getFormasPagamento(int index) {
            return contas.get(index);
        }

        @Override
        public Object getValueAt(int i, int i1) {

            FormasPagamento forma = (FormasPagamento) contas.get(i);

            switch (i1) {
                case 0:
                    return forma.getId();

                case 1:
                    return forma.getFormaPagamento();

            }
            return null;
        }

        public void busca(String texto) {

            FormasPagamentoDAO dao = new FormasPagamentoDAO();
            List<FormasPagamento> p = new ArrayList<>();
            p = dao.listar();

            Pattern padrao = Pattern.compile(".*" + texto + ".*", Pattern.CASE_INSENSITIVE); // criou padrao .* e qualquer coisa antes ou depois

            Iterator i = p.iterator();

            while (i.hasNext()) {
                FormasPagamento forma = (FormasPagamento) i.next();
                Matcher pesquisa = padrao.matcher(forma.getFormaPagamento()); // vai comparar com o getnome

                if (!pesquisa.matches()) { // resultado da pesquisa
                    i.remove();             // se nao combimou remove.
                }
            }

            // se caso dar erro ao retornar os registros na busca
            this.contas = p;
            fireTableDataChanged();
        }
    }
